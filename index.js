function MP_MobileMenuHandler() {
    var opened = false;

    function MP_MobileMenuHandler_ScrollOpener() {
        $( "li.navTab.ResponsiveMenu-mobile" ).click(function() {
            //responsive scroll to nav and open nav if clicked on nav icon

            //on click of responsive nav toggle button fixed sticky nav
            
                if ($(".www-mp-nav-item.responsive-menu").siblings(".www-mp-nav-item").css('display') == 'list-item') {
                // if the responsive nav is open..
                
                var scrolllimit = 100;
                
                    
                    if ($(window).scrollTop() > scrolllimit) {
                    // if the user has scrolled away more than specified number
                    // pixels from the top of the window
                    // and clicks the responsive toggle button again
                    // simply scroll back up to the menu without toggling it
                        
                        $([document.documentElement, document.body]).animate({
                            scrollTop: $("ul.www-mp-nav").offset().top
                        }, 1000);
                        
                    } else {
                        // else the user most likely intends to close the
                        // responsive menu, so close the responsive menu
                        // by changing its css styles
                    
                        MP_MobileMenu_close();
                    }
    
                } else {
                    // else if the responsive nav isn't open
                    // then the user intends to open it
                    // so we toggle the css classes to show it
                    
                    MP_MobileMenu_open();
                    
                    setTimeout( function() {
                        // and also scroll the user to the nav on click
                        $([document.documentElement, document.body]).animate({
                            scrollTop: $("ul.www-mp-nav").offset().top
                        }, 1000);
                    
                    }, 100);
                }
    
        });
    }

    function MP_MobileMenu_open() {
        if (!opened) {
            $(".www-mp-nav-item.responsive-menu").siblings(".www-mp-nav-item").css( "display", "list-item" );
            opened = true;
        }
    }

    function MP_MobileMenu_close() {
        if (opened) {
            $(".www-mp-nav-item.responsive-menu").siblings(".www-mp-nav-item").css( "display", "none" );
            opened = false;
        }
    }

    MP_MobileMenuHandler_ScrollOpener();

    $(".responsive-menu .www-mp-nav-button").click(function() {
        if (!opened) {
            MP_MobileMenu_open();
        } else {
            MP_MobileMenu_close();
        }
    });

    $("div#content").click(function() {
        MP_MobileMenu_close();
    });

}


