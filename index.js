$(window).load(function () { 
// Home page animated social media buttons sidebar

	setTimeout( function() {
		// Timer exists to account for JS adding in news feed
		// w/o it the document height would return as the height
		// without the news items

		$(function() {

			var $sidebar   = $(".www-mp-social-tray"), 
				$window    = $(window),
				offset     = $sidebar.offset(),
				// Additional padding to top of window on scroll
				topPadding = 200,
				footerHeight = $(".www-mp-footer").height(),
				pageHeight = $(document).height(),
				socialHeight = $sidebar.height(),
				// Additional padding on bottom of page
				bottomlimit = 0,
				scrollBottom = (pageHeight - footerHeight - socialHeight - topPadding - bottomlimit);

			$window.scroll(function() {
				if( $(window).width() > 840) {
				// don't bother counting this for responsive
				
					if ($window.scrollTop() > offset.top) {
						// Debug
						// console.log( "Window ScrollTop: " + $window.scrollTop() + " Sidebar OffsetTop: " + offset.top + " Footer Height: " + footerHeight + " PageHeight: " + pageHeight + " ScrollBottom: " + scrollBottom );
						
						// If the scroll offcet does not exceed the height of the footer
						// Preventing overlap of the social bar on the footer
						// On certain resolutions
						if ($window.scrollTop() < scrollBottom ) {
							// .. Then animate
							$sidebar.stop().animate({
								marginTop: $window.scrollTop() - offset.top + topPadding
							});
						}
						// Else stick to the latest position by default
					} else {
						// Or if not scrolled down enough to start animating
						// Don't have any offset whatsoever
						$sidebar.stop().animate({
							marginTop: 0
						});
					}
				}
			});
			
		});
	}, 1000);
});

