function MP_UpdateEditPrivateMessage() {
    // Duplicate message prevention for private messages on edit
    // Simplar to preventDupEdit in MP_UpdateThreadonThreadAction

    $(document).ajaxSuccess(function( event, xhr, settings ) {
        var ajaxRegex = /^\/conversations\/\w+\.\d+\/save-message\?m=(\d+)$/,
            Cpath = settings.url.slice((settings.url.indexOf(document.domain) + document.domain.length), settings.url.length);

        if (Cpath.match(ajaxRegex)) {

            var allPostID = [];

            $( ".messageList .message" ).each(function() {
                // get all the post IDs of all the forum posts currently
                // on the page and push them to an array
                if($(this).is('[class*="message-"]')) {
                    var ClassList =  $(this).attr('class');
                    var PostID = "message-"+(ClassList.split("message-").pop());
                    allPostID.push(PostID);
                }
            });

            var allPostIDArray = allPostID.sort(); 
            var allPostIDDuplicate = [];

            for (var i = 0; i < allPostIDArray.length - 1; i++) {
                // then go through every item in the array and check for
                // duplicate occurences
                if (allPostIDArray[i + 1] == allPostIDArray[i]) {
                    allPostIDDuplicate.push(allPostIDArray[i]);
                    // if a duplicate is found, push it into the 
                    // duplicate holder array
                    
                    if (navigator.onLine) {
                        // now run another check to see if user is still online
                        // after all this if online, safely hide all duplicate
                        // posts regardless of amount of edits done
                        var editPostClass = Cpath.replace(ajaxRegex, '$1');
                        editPostClass = "message-" + editPostClass;

                        $("." + editPostClass).css( "display", "none" );
                        $("." + editPostClass).first().css( "display", "block" );

                        var count = 0;
                        var count = (count + 1) % allPostIDDuplicate.length;
                        
                        // this deals with duplication glitch outside
                        // of user's last post. set up a variable to go
                        // through every duplicate found currently in our array
                        

                        if (editPostClass != (allPostIDDuplicate[count])) {
                            // if we find a duplicate that is not the one we just
                            // hid for our current user, hide this duplicate as well
                            // and leave the most recent version
                            
                            $("."+(allPostIDDuplicate[count])).css( "display", "none" );
                            $("."+(allPostIDDuplicate[count])).last().css( "display", "block" );
                        } else {
                        }
                        
                    }
                }
            }

        }

    });
}


